package application;

public class Flight {
	
	String a;
	String b;
	String c;
	String d;
	
	public Flight(String a, String b, String c, String d) {
		
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}
	
	public  String ToString()
	{
		return a + "\t"+ b+"\t\t"+c+"\t\t\t"+d;
	}
	
	
	
	
	
	

}
